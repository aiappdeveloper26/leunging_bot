# ── database ─────────────────────────────────────────────────────────────────
def db():
    c = sqlite3.connect(DB)
    c.row_factory = sqlite3.Row
    return c

def init_db():
    with db() as c:
        c.executescript("""
        CREATE TABLE IF NOT EXISTS users(
            chat_id INTEGER PRIMARY KEY, active_deck INTEGER,
            digest_h INTEGER DEFAULT 8, digest_m INTEGER DEFAULT 0,
            streak INTEGER DEFAULT 0, streak_day TEXT);
        CREATE TABLE IF NOT EXISTS decks(
            id INTEGER PRIMARY KEY AUTOINCREMENT, chat_id INTEGER,
            name TEXT, topic TEXT, level TEXT, kind TEXT, lang TEXT);
        CREATE TABLE IF NOT EXISTS cards(
            id INTEGER PRIMARY KEY AUTOINCREMENT, chat_id INTEGER, deck_id INTEGER,
            front TEXT, back TEXT, example TEXT, hint TEXT, created REAL,
            interval REAL DEFAULT 0, ease REAL DEFAULT 2.5, reps INTEGER DEFAULT 0,
            lapses INTEGER DEFAULT 0, due REAL, last_reviewed REAL);
        CREATE TABLE IF NOT EXISTS newlog(
            chat_id INTEGER, deck_id INTEGER, last_new TEXT,
            PRIMARY KEY(chat_id, deck_id));
        """)

def ensure_user(chat_id):
    with db() as c:
        c.execute("INSERT OR IGNORE INTO users(chat_id) VALUES(?)", (chat_id,))

def get_user(chat_id):
    with db() as c:
        return c.execute("SELECT * FROM users WHERE chat_id=?", (chat_id,)).fetchone()

def set_active(chat_id, deck_id):
    with db() as c:
        c.execute("UPDATE users SET active_deck=? WHERE chat_id=?", (deck_id, chat_id))

def list_decks(chat_id):
    with db() as c:
        return c.execute("SELECT * FROM decks WHERE chat_id=? ORDER BY id", (chat_id,)).fetchall()

def get_deck(deck_id):
    with db() as c:
        return c.execute("SELECT * FROM decks WHERE id=?", (deck_id,)).fetchone()

def active_deck(chat_id):
    u = get_user(chat_id)
    if not u or not u["active_deck"]:
        return None
    return get_deck(u["active_deck"])

def add_deck(chat_id, d):
    with db() as c:
        cur = c.execute(
            "INSERT INTO decks(chat_id,name,topic,level,kind,lang) VALUES(?,?,?,?,?,?)",
            (chat_id, d["name"], d["topic"], d["level"], d["kind"], d.get("lang")))
        return cur.lastrowid

def deck_cards(chat_id, deck_id):
    with db() as c:
        return c.execute("SELECT * FROM cards WHERE chat_id=? AND deck_id=? ORDER BY id",
                         (chat_id, deck_id)).fetchall()

def due_cards(chat_id, deck_id, now):
    with db() as c:
        return c.execute("SELECT * FROM cards WHERE chat_id=? AND deck_id=? AND due<=? ORDER BY due",
                         (chat_id, deck_id, now)).fetchall()

def get_card(card_id):
    with db() as c:
        return c.execute("SELECT * FROM cards WHERE id=?", (card_id,)).fetchone()

def insert_card(chat_id, deck_id, card, sched, now):
    with db() as c:
        cur = c.execute(
            "INSERT INTO cards(chat_id,deck_id,front,back,example,hint,created,interval,ease,reps,lapses,due,last_reviewed)"
            " VALUES(?,?,?,?,?,?,?,?,?,?,?,?,?)",
            (chat_id, deck_id, card["front"], card["back"], card.get("example",""), card.get("hint",""),
             now, sched["interval"], sched["ease"], sched["reps"], sched["lapses"], sched["due"], now))
        return cur.lastrowid

def update_sched(card_id, s):
    with db() as c:
        c.execute("UPDATE cards SET interval=?,ease=?,reps=?,lapses=?,due=?,last_reviewed=? WHERE id=?",
                  (s["interval"], s["ease"], s["reps"], s["lapses"], s["due"], s["last_reviewed"], card_id))

def mark_new_today(chat_id, deck_id, day):
    with db() as c:
        c.execute("INSERT INTO newlog(chat_id,deck_id,last_new) VALUES(?,?,?) "
                  "ON CONFLICT(chat_id,deck_id) DO UPDATE SET last_new=?", (chat_id, deck_id, day, day))

def got_new_today(chat_id, deck_id, day):
    with db() as c:
        r = c.execute("SELECT last_new FROM newlog WHERE chat_id=? AND deck_id=?", (chat_id, deck_id)).fetchone()
        return bool(r and r["last_new"] == day)

def all_user_ids():
    with db() as c:
        return [r["chat_id"] for r in c.execute("SELECT chat_id FROM users").fetchall()]

# ── time / streak ────────────────────────────────────────────────────────────
def now_ts():
    return time.time()

def today_str():
    return dt.datetime.now(TZ).date().isoformat()

def touch_streak(chat_id):
    u = get_user(chat_id)
    t = today_str()
    if u["streak_day"] == t:
        return u["streak"]
    yest = (dt.datetime.now(TZ).date() - dt.timedelta(days=1)).isoformat()
    new = u["streak"] + 1 if u["streak_day"] == yest else 1
    with db() as c:
        c.execute("UPDATE users SET streak=?, streak_day=? WHERE chat_id=?", (new, t, chat_id))
    return new

# ── spaced repetition (SM-2, lightly tuned) ──────────────────────────────────
def schedule(card, rating, t):
    interval = card["interval"] if card else 0
    ease     = card["ease"] if card else 2.5
    reps     = card["reps"] if card else 0
    lapses   = card["lapses"] if card else 0
    if rating == "again":
        return dict(interval=0, ease=max(1.3, ease-0.2), reps=0, lapses=lapses+1, due=t+60, last_reviewed=t)
    if rating == "hard":
        ease = max(1.3, ease-0.15); interval = 1 if reps == 0 else max(1, round(interval*1.2))
    elif rating == "good":
        interval = 1 if reps == 0 else (3 if reps == 1 else round(interval*ease))
    elif rating == "easy":
        ease += 0.15; interval = 2 if reps == 0 else (4 if reps == 1 else round(interval*ease*1.3))
    return dict(interval=interval, ease=ease, reps=reps+1, lapses=lapses,
                due=t+interval*DAY, last_reviewed=t)

def fmt_days(d):
    if d <= 0: return "now"
    if d < 1:  return f"{round(d*24)}h"
    if d < 30: return f"{round(d)}d"
    if d < 365:return f"{round(d/30)}mo"
    return f"{d/365:.1f}y"

def preview(card, rating):
    return "<10m" if rating == "again" else fmt_days(schedule(card, rating, 0)["interval"])

# ── Groq ──────────────────────────────────────────────────────────────────────
def _groq_sync(system, user, json_out=True):
    key = os.environ["GROQ_API_KEY"]
    url = "https://api.groq.com/openai/v1/chat/completions"
    body = {
        "model": MODEL,
        "messages": [
            {"role": "system", "content": system},
            {"role": "user", "content": user},
        ],
        "temperature": 0.9,
    }
    if json_out:
        body["response_format"] = {"type": "json_object"}
    r = requests.post(url, headers={"Content-Type": "application/json",
                                     "Authorization": f"Bearer {key}"},
                      json=body, timeout=40)
    if r.status_code != 200:
        detail = ""
        try: detail = r.json().get("error", {}).get("message", "")
        except Exception: pass
        if r.status_code == 429:
            raise RuntimeError("Hit the Groq free-tier rate limit — try again in a minute.")
        if r.status_code == 401:
            raise RuntimeError("Groq API key was rejected — check GROQ_API_KEY.")
        raise RuntimeError(f"Groq error {r.status_code}: {detail}")
    data = r.json()
    return data["choices"][0]["message"]["content"]

async def groq(system, user, json_out=True):
    return await asyncio.to_thread(_groq_sync, system, user, json_out)

def parse_json(text):
    t = text.replace("```json", "").replace("```", "").strip()
    a, b = t.find("{"), t.rfind("}")
    return json.loads(t[a:b+1] if a >= 0 else t)

async def gen_card(deck, existing_fronts):
    avoid = ("Do NOT repeat any of these already-covered items: "
             + "; ".join(existing_fronts[-40:]) + ".") if existing_fronts else ""
    if deck["kind"] == "vocab":
        lang = deck["lang"]
        shape = (f"front = the {lang} word written naturally "
                 f"(for Japanese, give the word in kanji/kana followed by its reading in parentheses; "
                 f"for French, include the gender article such as le/la/un/une), "
                 f"back = English meaning plus part of speech, "
                 f"example = a natural short sentence in {lang} using the word (add a reading/translation if the script isn't Latin), "
                 f"hint = a brief memory aid.")
    else:
        shape = ("front = the term or concept name, back = a clear 1-2 sentence explanation, "
                 "example = a concrete example or tiny code snippet that makes it click, "
                 "hint = a one-line intuition.")
    system = ("You are a precise, encouraging tutor building ONE spaced-repetition flashcard. "
              "Reply with ONLY a JSON object with keys: front, back, example, hint. Keep each field tight.")
    user = (f"Topic: {deck['topic']}. Level: {deck['level']}. "
            f"Make one genuinely useful, NEW flashcard. {shape} {avoid}")
    return parse_json(await groq(system, user))

async def grade(card, answer):
    system = ('You grade a learner\'s recall attempt generously but honestly. '
              'Reply with ONLY JSON: {"correct": boolean, "feedback": string}. '
              'Feedback is one warm, specific sentence.')
    user = (f'Prompt shown: "{card["front"]}". Expected answer: "{card["back"]}". '
            f'The learner wrote: "{answer}". Was it essentially right?')
    return parse_json(await groq(system, user))

# ── formatting ────────────────────────────────────────────────────────────────
def card_front_md(card, tag=""):
    head = f"<i>{tag}</i>\n" if tag else ""
    hint = f"\n<i>{card['hint']}</i>" if card["hint"] else ""
    return f"{head}<b>{card['front']}</b>{hint}"

def card_back_md(card):
    ex = f"\n\n<code>{card['example']}</code>" if card["example"] else ""
    return f"<b>{card['front']}</b>\n\n{card['back']}{ex}"

def rate_keyboard(card):
    row = [InlineKeyboardButton(f"{r.title()} · {preview(card, r)}", callback_data=f"rate:{card['id']}:{r}")
           for r in ("again", "hard", "good", "easy")]
    return InlineKeyboardMarkup([row[:2], row[2:]])

