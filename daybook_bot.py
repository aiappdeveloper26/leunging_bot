#!/usr/bin/env python3
"""
The Daybook — a "learn one thing a day" Telegram bot.

Each morning it pushes you a fresh flashcard on a subject you're studying,
generated by the Google Gemini API. Older cards resurface on a widening
spaced-repetition schedule (SM-2). You can also review or quiz on demand.

Setup (see the README notes in chat):
  pip install -r requirements.txt
  export TELEGRAM_BOT_TOKEN="123:ABC..."     # from @BotFather
  export GROQ_API_KEY="gsk_..."              # from console.groq.com/keys
  export DAYBOOK_TZ="Asia/Tokyo"             # optional, defaults to UTC
  python daybook_bot.py
"""

