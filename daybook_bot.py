async def deliver_new_card(chat, ctx, prefix=""):
    deck = active_deck(chat)
    bot = ctx.bot
    if not deck:
        return await bot.send_message(chat, "Pick a subject first with /start or /deck.")
    if got_new_today(chat, deck["id"], today_str()):
        prefix = prefix or "Today's seed is already planted — here's one more anyway.\n\n"
    msg = await bot.send_message(chat, prefix + "✦ Drawing a card…")
    topic, done, total = syllabus_progress(deck)
    try:
        existing = [c["front"] for c in deck_cards(chat, deck["id"])]
        data = await gen_card(deck, existing, syllabus_topic=topic,
                              syllabus_done=(total > 0 and topic is None))
    except Exception as e:
        return await msg.edit_text(f"Couldn't draw a card: {e}")
    base = {"interval": 0, "ease": 2.5, "reps": 0, "lapses": 0}
    sched = schedule(base, "good", now_ts())          # first sight → returns tomorrow
    cid = insert_card(chat, deck["id"], data, sched, now_ts())
    mark_new_today(chat, deck["id"], today_str())
    touch_streak(chat)
    if topic is not None:
        advance_syllabus(deck["id"])
        tag = f"Today · {deck['name']} · {done+1}/{total}"
    elif total:
        tag = f"Today · {deck['name']} · bonus (syllabus complete)"
    else:
        tag = f"Today · {deck['name']}"
    card = get_card(cid)
    await msg.edit_text(
        card_front_md(card, tag),
        parse_mode=ParseMode.HTML,
        reply_markup=InlineKeyboardMarkup([[InlineKeyboardButton("Show answer", callback_data=f"show:{cid}")]]))

async def cmd_review(update: Update, ctx):
    chat = update.effective_chat.id
    deck = active_deck(chat)
    if not deck:
        return await update.message.reply_text("Pick a subject first with /deck.")
    due = due_cards(chat, deck["id"], now_ts())
    if not due:
        total = len(deck_cards(chat, deck["id"]))
        return await update.message.reply_text(
            f"All caught up. {total} card{'s' if total != 1 else ''} waiting in the horizon. ✨")
    ctx.user_data["rqueue"] = [c["id"] for c in due]
    await send_next_review(chat, ctx)

async def send_next_review(chat, ctx):
    q = ctx.user_data.get("rqueue", [])
    if not q:
        streak = get_user(chat)["streak"]
        return await ctx.bot.send_message(chat, f"Review done. 🔥 {streak}-day streak.")
    card = get_card(q[0])
    if not card:
        q.pop(0); return await send_next_review(chat, ctx)
    await ctx.bot.send_message(
        chat, card_front_md(card, f"Review · {len(q)} left"),
        parse_mode=ParseMode.HTML,
        reply_markup=InlineKeyboardMarkup([[InlineKeyboardButton("Reveal", callback_data=f"rev:{card['id']}")]]))

async def cmd_quiz(update: Update, ctx):
    chat = update.effective_chat.id
    deck = active_deck(chat)
    if not deck:
        return await update.message.reply_text("Pick a subject first with /deck.")
    cards = deck_cards(chat, deck["id"])
    if not cards:
        return await update.message.reply_text("Nothing to quiz yet — /learn a card first.")
    due = due_cards(chat, deck["id"], now_ts())
    card = random.choice(due if due else cards)
    ctx.user_data["quiz_card"] = card["id"]
    await update.message.reply_text(
        card_front_md(card, "Quiz · type your answer"), parse_mode=ParseMode.HTML)

async def cmd_settime(update: Update, ctx):
    chat = update.effective_chat.id
    ensure_user(chat)
    try:
        h, m = map(int, ctx.args[0].split(":"))
        assert 0 <= h < 24 and 0 <= m < 60
    except Exception:
        return await update.message.reply_text("Use /settime HH:MM, e.g. /settime 07:30")
    with db() as c:
        c.execute("UPDATE users SET digest_h=?, digest_m=? WHERE chat_id=?", (h, m, chat))
    await schedule_digest(ctx.application, chat)
    await update.message.reply_text(
        f"Morning card set for {h:02d}:{m:02d} ({TZ.key}). ☀️")

async def cmd_syllabus(update: Update, ctx):
    chat = update.effective_chat.id
    deck = active_deck(chat)
    if not deck:
        return await update.message.reply_text("Pick a subject first with /deck.")
    topic, done, total = syllabus_progress(deck)
    if not total:
        return await update.message.reply_text(
            f"<b>{deck['name']}</b> doesn't follow a fixed syllabus — daily cards are picked freely "
            f"by the tutor (avoiding repeats).", parse_mode=ParseMode.HTML)
    bar_len = 20
    filled = round(bar_len * done / total)
    bar = "█"*filled + "░"*(bar_len-filled)
    lines = [f"<b>{deck['name']} syllabus</b>", f"<code>{bar}</code> {done}/{total} topics covered", ""]
    if topic:
        lines.append(f"<b>Next up:</b> {topic}")
    else:
        lines.append("<b>Core curriculum complete!</b> Now sending bonus/advanced cards.")
    syl = SYLLABI[deck["syllabus_key"]]
    if done > 0:
        lines.append("")
        lines.append("<i>Recent:</i> " + ", ".join(syl[max(0, done-3):done]))
    await update.message.reply_text("\n".join(lines), parse_mode=ParseMode.HTML)

async def cmd_stats(update: Update, ctx):
    chat = update.effective_chat.id
    deck = active_deck(chat)
    u = get_user(chat)
    if not deck:
        return await update.message.reply_text(f"🔥 {u['streak']}-day streak.\nPick a subject with /deck.")
    cards = deck_cards(chat, deck["id"])
    buckets = [("Due", 0), ("1d", 1.5), ("3d", 4), ("1wk", 9), ("2wk", 20), ("1mo", 45), ("Far", 1e9)]
    counts = [0]*len(buckets)
    for c in cards:
        d = (c["due"] - now_ts())/DAY
        for i, (_, mx) in enumerate(buckets):
            if d <= mx: counts[i] += 1; break
    horizon = "\n".join(f"  {lbl:>4} │ {'●'*n}{'' if n else '·'}  {n or ''}"
                        for (lbl, _), n in zip(buckets, counts))
    await update.message.reply_text(
        f"<b>{deck['name']}</b>\n🔥 {u['streak']}-day streak · {len(cards)} cards\n\n"
        f"<b>Memory horizon</b>\n<code>{horizon}</code>\n\n"
        f"<i>Cards drift right as you get them right.</i>",
        parse_mode=ParseMode.HTML)

