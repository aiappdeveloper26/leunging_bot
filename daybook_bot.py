async def cmd_start(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    chat = update.effective_chat.id
    ensure_user(chat)
    await update.message.reply_text(
        "<b>The Daybook</b> 🌿\nOne thing a day, made to stick.\n\n"
        "Each morning I'll send a fresh card on a subject you're studying, and resurface "
        "older ones right before you'd forget them. Pick a subject to begin:",
        parse_mode=ParseMode.HTML, reply_markup=preset_keyboard())
    await schedule_digest(ctx.application, chat)

def preset_keyboard():
    rows = [[InlineKeyboardButton(p["name"], callback_data=f"preset:{i}")] for i, p in enumerate(PRESETS)]
    rows.append([InlineKeyboardButton("➕ Custom subject", callback_data="newdeck")])
    return InlineKeyboardMarkup(rows)

