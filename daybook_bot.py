# ── callback queries ──────────────────────────────────────────────────────────
async def on_callback(update: Update, ctx):
    q = update.callback_query
    await q.answer()
    chat = q.message.chat.id
    data = q.data

    if data.startswith("preset:"):
        ensure_user(chat)
        p = PRESETS[int(data.split(":")[1])]
        did = add_deck(chat, p); set_active(chat, did)
        await q.edit_message_text(f"Studying <b>{p['name']}</b>. Pulling your first card…", parse_mode=ParseMode.HTML)
        return await deliver_new_card(chat, ctx)

    if data.startswith("setdeck:"):
        did = int(data.split(":")[1]); set_active(chat, did)
        return await q.edit_message_text(f"Switched to <b>{get_deck(did)['name']}</b>.", parse_mode=ParseMode.HTML)

    if data == "newdeck":
        ctx.user_data["nd"] = {"step": "name"}
        return await ctx.bot.send_message(chat, "New subject — what should I call it? (e.g. <i>Italian verbs</i>)", parse_mode=ParseMode.HTML)

    if data.startswith("ndk:"):
        nd = ctx.user_data.get("nd", {}); nd["kind"] = data.split(":")[1]
        if nd["kind"] == "vocab":
            nd["step"] = "lang"
            return await ctx.bot.send_message(chat, "Which language?")
        nd["step"] = "level"
        return await ctx.bot.send_message(chat, "Level?", reply_markup=level_keyboard())

    if data.startswith("ndl:"):
        nd = ctx.user_data.get("nd", {}); nd["level"] = data.split(":")[1]
        did = add_deck(chat, {"name": nd["name"], "topic": nd["topic"], "level": nd["level"],
                              "kind": nd["kind"], "lang": nd.get("lang")})
        set_active(chat, did); ctx.user_data.pop("nd", None)
        await q.edit_message_text(f"Created <b>{nd['name']}</b>. Pulling your first card…", parse_mode=ParseMode.HTML)
        return await deliver_new_card(chat, ctx)

    if data.startswith("show:"):
        card = get_card(int(data.split(":")[1]))
        return await q.edit_message_text(card_back_md(card), parse_mode=ParseMode.HTML)

    if data.startswith("rev:"):
        card = get_card(int(data.split(":")[1]))
        return await q.edit_message_text(card_back_md(card), parse_mode=ParseMode.HTML,
                                         reply_markup=rate_keyboard(card))

    if data.startswith("rate:"):
        _, cid, rating = data.split(":")
        card = get_card(int(cid))
        if card:
            update_sched(card["id"], schedule(card, rating, now_ts()))
            touch_streak(chat)
        nxt = schedule(card, rating, now_ts())["interval"] if card else 0
        await q.edit_message_text(
            card_back_md(card) + f"\n\n<i>{rating} · next in {preview(card, rating)}</i>",
            parse_mode=ParseMode.HTML)
        qq = ctx.user_data.get("rqueue", [])
        if qq:
            done = qq.pop(0)
            if rating == "again": qq.append(done)   # resurface this session
        return await send_next_review(chat, ctx)

def level_keyboard():
    return InlineKeyboardMarkup([[InlineKeyboardButton(l, callback_data=f"ndl:{l}")
                                  for l in ("beginner", "intermediate", "advanced")]])

# ── free-text handler (custom-deck steps + quiz answers) ──────────────────────
async def on_text(update: Update, ctx):
    chat = update.effective_chat.id
    text = (update.message.text or "").strip()
    nd = ctx.user_data.get("nd")
    if nd:
        if nd["step"] == "name":
            nd["name"] = text; nd["step"] = "topic"
            return await update.message.reply_text("What should I draw cards from? Describe the topic.")
        if nd["step"] == "topic":
            nd["topic"] = text; nd["step"] = "kind"
            kb = InlineKeyboardMarkup([
                [InlineKeyboardButton("Concepts", callback_data="ndk:concept"),
                 InlineKeyboardButton("Terms", callback_data="ndk:term")],
                [InlineKeyboardButton("Vocab", callback_data="ndk:vocab"),
                 InlineKeyboardButton("Deep lesson 📖", callback_data="ndk:lesson")]])
            return await update.message.reply_text(
                "What kind of cards?\n<i>Deep lesson = a few short paragraphs with a worked example "
                "and exam-style takeaway — good for certifications, investing, etc.</i>",
                parse_mode=ParseMode.HTML, reply_markup=kb)
        if nd["step"] == "lang":
            nd["lang"] = text; nd["step"] = "level"
            return await update.message.reply_text("Level?", reply_markup=level_keyboard())
        return

    cid = ctx.user_data.pop("quiz_card", None)
    if cid:
        card = get_card(cid)
        if not card:
            return await update.message.reply_text("That card vanished — try /quiz again.")
        thinking = await update.message.reply_text("Checking…")
        try:
            g = await grade(card, text)
            mark = "✅" if g.get("correct") else "❌"
            rating = "good" if g.get("correct") else "again"
            update_sched(card["id"], schedule(card, rating, now_ts())); touch_streak(chat)
            await thinking.edit_text(
                f"{mark} {g.get('feedback','')}\n\n{card_back_md(card)}\n\n"
                f"<i>scored as {rating} · next in {preview(card, rating)}</i>",
                parse_mode=ParseMode.HTML)
        except Exception as e:
            await thinking.edit_text(f"Couldn't grade that ({e}).\n\n{card_back_md(card)}", parse_mode=ParseMode.HTML)
        return

    await update.message.reply_text("Try /learn, /review, /quiz, or /help.")

# ── morning digest job ────────────────────────────────────────────────────────
async def digest_job(ctx: ContextTypes.DEFAULT_TYPE):
    chat = ctx.job.data
    deck = active_deck(chat)
    if not deck:
        return
    due = len(due_cards(chat, deck["id"], now_ts()))
    greeting = f"☀️ <b>Good morning.</b> Today in <b>{deck['name']}</b>:"
    await ctx.bot.send_message(chat, greeting, parse_mode=ParseMode.HTML)
    if not got_new_today(chat, deck["id"], today_str()):
        await deliver_new_card(chat, ctx)
    if due:
        await ctx.bot.send_message(
            chat, f"You also have {due} card{'s' if due != 1 else ''} due — /review when you're ready.")

async def schedule_digest(app: Application, chat):
    u = get_user(chat)
    if not u:
        return
    for j in app.job_queue.get_jobs_by_name(f"digest:{chat}"):
        j.schedule_removal()
    app.job_queue.run_daily(
        digest_job, time=dt.time(u["digest_h"], u["digest_m"], tzinfo=TZ),
        data=chat, name=f"digest:{chat}")

async def on_startup(app: Application):
    init_db()
    # Defensive: if a webhook was ever set for this token (e.g. by a previous
    # deploy or a different bot framework), polling will conflict with it.
    # Clearing it here is harmless if no webhook is set.
    try:
        await app.bot.delete_webhook(drop_pending_updates=False)
    except Exception as e:
        log.warning("delete_webhook failed (continuing anyway): %s", e)
    for chat in all_user_ids():
        await schedule_digest(app, chat)
    log.info("Daybook ready. Rescheduled digests for %d users.", len(all_user_ids()))

# ── health-check server (for Render's free Web Service tier) ────────────────
class _HealthHandler(BaseHTTPRequestHandler):
    def do_GET(self):
        self.send_response(200)
        self.send_header("Content-Type", "text/plain")
        self.end_headers()
        self.wfile.write(b"The Daybook is running.")
    def log_message(self, *a, **k):
        pass  # keep request logs out of the main log stream

def start_health_server():
    port = int(os.getenv("PORT", "10000"))
    server = ThreadingHTTPServer(("0.0.0.0", port), _HealthHandler)
    thread = threading.Thread(target=server.serve_forever, daemon=True)
    thread.start()
    log.info("Health-check server listening on port %d (for Render).", port)

